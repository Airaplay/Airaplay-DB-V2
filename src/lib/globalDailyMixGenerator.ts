/**
 * Global Daily Mix Generator
 * 
 * Generates daily mixes for non-authenticated users and users without listening history
 * Uses global trending data, top genres, and editorial curation
 */

import { supabase } from './supabase';
import { fetchWithCache, CACHE_KEYS, CACHE_TTL, configCache } from './configCache';

interface GlobalDailyMix {
  id: string;
  mix_number: number;
  title: string;
  description: string;
  genre_focus: string | null;
  mood_focus: string | null;
  cover_image_url: string | null;
  track_count: number;
  generated_at: string;
  artist_images?: string[];
  display_title?: string;
}

interface GlobalMixTrack {
  mix_id: string;
  song_id: string;
  position: number;
  explanation: string;
}

interface SongRow {
  id: string;
  title: string;
  artist_id: string | null;
  cover_image_url: string | null;
  duration_seconds: number | null;
  audio_url: string | null;
  play_count: number | null;
  created_at: string | null;
}

type SongGenreMap = Map<string, string[]>;

